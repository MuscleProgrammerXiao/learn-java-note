public class ChangeChar{
	public static void main(String[] args) {
		// \t 一个制表位，实现对齐的功能
		System.out.println("北京\t天津\t上海");	
		// \n 换行符
		System.out.println("jack\nmary");
		// \\：一个\
		System.out.println("C:\\window\\system\\text.exe");
		// \'：一个'
		System.out.println("mary说\'你好吗\'");
		// \"：一个"
		System.out.println("jack说\"你好啊\"");
		// \r：一个回车 System.out.println("我是java开发者\r重庆");
		System.out.println("我是java开发者\r重庆");

		//举例
		System.out.println("书名\t作者\t价格\t销量\n三国\t罗贯中\t120\t1000");
	}
}